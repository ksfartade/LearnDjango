public class Addition {
    public static void main(String[] args) {
        // Declare two variables and assign values
        int num1 = 10;
        int num2 = 20;

        // Perform addition
        int sum = num1 + num2;

        // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}

